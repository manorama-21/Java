class primitive_dtype {
     public static void main(String args[]){

int a = 8;  //The int data type can have values from -231 to 231-1 (32-bit signed two's complement integer).
System.out.println("Integer:"+a);

byte b = 127;   //The byte data type can have values from -128 to 127 (8-bit signed two's complement integer).
System.out.println("Byte:"+b);

short c = 32723;  //The short data type in Java can have values from -32768 to 32767 (16-bit signed two's complement integer).
System.out.println("Short:"+c);

long d = 886433465;  //The long data type can have values from -263 to 263-1 (64-bit signed two's complement integer).
System.out.println("Long:"+d);

float e = 8;  //The float data type is a single-precision 32-bit floating-point.
float e1 = 8.4f;
System.out.println("Float:"+e);
System.out.println("Float:"+e1);

double f = 40;  //The double data type is a double-precision 64-bit floating-point.
double f1 = 40.4d; 
System.out.println("double:"+f);
System.out.println("double:"+f1);

boolean g = true;  //The boolean data type has two possible values, either true or false.
boolean h = false;
System.out.println("Boolean:"+g);
System.out.println("Boolean:"+h);

char i = '8';   //Stores a single character/letter 
char j ='m';
System.out.println("Character:"+i);
System.out.println("Character:"+j);
 }}
